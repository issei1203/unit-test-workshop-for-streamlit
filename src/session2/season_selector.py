def select_from(month: int) -> str:
    """
    指定された月の季節を返す。（春、夏、秋、冬）
    月の値が 1～12 の範囲外の場合は ValueError 例外を発生させる。
    """

    # --- 分岐1: 冬 (12月, 1月, 2月) ---
    if month in [12, 1, 2]:
        return "冬"

    # --- 分岐2: 春 (3月, 4月, 5月) ---
    if month in [3, 4, 5]:
        return "春"

    # --- 分岐3: 夏 (6月, 7月, 8月) ---
    if month in [6, 7, 8]:
        return "夏"

    # --- 分岐4: 秋 (9月, 10月, 11月) ---
    if month in [9, 10, 11]:
        return "秋"

    # --- 分岐5: どの季節にも該当しない (1～12の範囲外) 場合 ---
    else:
        # 例外を発生させる（異常系）
        raise ValueError(f"不正な月が入力されました: {month}。月は1から12の範囲で指定してください。")